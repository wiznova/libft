int		ft_isalnum(int c)
{
	int alpha;

	alpha = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		alpha = 1;
	if ((c >= '0' && c <= '9') || alpha == 1)
		return (1);
	return (0);
}
